/**
 * Represents link information in JDownloader.
 * 
 * @class
 * @typedef {Object} LinkInfo
 * 
 * @method getDesc
 *   @returns {string} Description of the link.
 * @method getGroup
 *   @returns {string} Group name of the link.
 * @method getPartNum
 *   @returns {Integer} Part number of the link.
 * @method name
 *   @returns {string} Name of the link.
 */
class LinkInfo {
    /**
     * @returns {string}
     */
    getDesc() {};

    /**
     * @returns {string}
     */
    getGroup() {};

    /**
     * @returns {Integer}
     */
    getPartNum() {};

    /**
     * @returns {string}
     */
    name() {};
}